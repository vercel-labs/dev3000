  devAgentInstructions?: string
  devAgentCompiledSpec?: DevAgentAshCompiledSpec
  devAgentAshTarballUrl?: string
  devAgentRevision?: number
  devAgentSpecHash?: string
  runnerKind?: "dev-agent" | "skill-runner"
