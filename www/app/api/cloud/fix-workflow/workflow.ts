async function saveDoneStatus(
  progressContext: ProgressContext,
  reportBlobUrl: string,
  prUrl: string | null,
  prError: string | null,
  sandboxUrl?: string | null
): Promise<void> {
  "use step"
  const existingRun = await getProgressRunSnapshot(progressContext)
  const reportSummary = await loadWorkflowReportSummary(reportBlobUrl)
  const progressLogs = dedupeProgressLogs(
    [...(existingRun?.progressLogs ?? []), ...(progressContext.progressLogs ?? [])],
    120
  )
  const nextRun = {
    ...existingRun,
    id: progressContext.runId,
    userId: progressContext.userId,
    projectName: progressContext.projectName,
    timestamp: progressContext.timestamp,
    status: "done",
    type:
      (progressContext.workflowType as
        | "cls-fix"
        | "prompt"
        | "design-guidelines"
        | "react-performance"
        | "url-audit"
        | "turbopack-bundle-analyzer") ||
      existingRun?.type ||
      "cls-fix",
    runnerKind: progressContext.runnerKind ?? existingRun?.runnerKind,
    devAgentId: progressContext.devAgentId,
    devAgentName: progressContext.devAgentName,
    devAgentDescription: progressContext.devAgentDescription,
    devAgentRevision: progressContext.devAgentRevision,
    devAgentSpecHash: progressContext.devAgentSpecHash,
    skillRunnerCanonicalPath: progressContext.skillRunnerCanonicalPath ?? existingRun?.skillRunnerCanonicalPath,
    skillRunnerValidationWarning:
      progressContext.skillRunnerValidationWarning ?? existingRun?.skillRunnerValidationWarning,
    devAgentExecutionMode: progressContext.devAgentExecutionMode,
    devAgentSandboxBrowser: progressContext.devAgentSandboxBrowser,
    stepNumber: Math.max(existingRun?.stepNumber ?? 0, progressContext.activeStepNumber ?? 0, 1),
    currentStep: "Workflow completed",
    completedAt: new Date().toISOString(),
    reportBlobUrl,
    costUsd: reportSummary?.costUsd ?? existingRun?.costUsd,
    successEvalResult: reportSummary?.successEvalResult ?? existingRun?.successEvalResult ?? null,
    clsScore: reportSummary?.clsScore ?? existingRun?.clsScore ?? null,
    afterClsScore: reportSummary?.afterClsScore ?? existingRun?.afterClsScore ?? null,
    beforeWebVitals: reportSummary?.beforeWebVitals ?? existingRun?.beforeWebVitals,
    afterWebVitals: reportSummary?.afterWebVitals ?? existingRun?.afterWebVitals,
    verificationStatus: reportSummary?.verificationStatus ?? existingRun?.verificationStatus,
    beforeScreenshots: reportSummary?.beforeScreenshots ?? existingRun?.beforeScreenshots,
    afterScreenshots: reportSummary?.afterScreenshots ?? existingRun?.afterScreenshots,
    beforeScreenshotUrl: reportSummary?.beforeScreenshotUrl ?? existingRun?.beforeScreenshotUrl,
    afterScreenshotUrl: reportSummary?.afterScreenshotUrl ?? existingRun?.afterScreenshotUrl,
    prUrl: prUrl || undefined,
    prError: prError || undefined,
    sandboxUrl: sandboxUrl || progressContext.sandboxUrl || existingRun?.sandboxUrl || undefined,
    progressLogs
  } satisfies WorkflowRun
  await persistWorkflowRun(nextRun, { mirrorTarget: progressContext.controlPlaneMirrorTarget })
  if (progressContext.runnerKind === "skill-runner" && progressContext.devAgentId) {
    try {
      const { recordSkillRunnerCompletion } = await import("@/lib/skill-runners")
      await recordSkillRunnerCompletion(progressContext.devAgentId, reportSummary?.costUsd ?? 0)
    } catch (error) {
      workflowLog(
        `[Workflow] Failed to record skill runner completion stats: ${error instanceof Error ? error.message : String(error)}`
      )
    }
    const telemetry = progressContext.skillRunnerTelemetry
    if (telemetry) {
      const durationMs = Math.max(0, Date.parse(nextRun.completedAt || "") - Date.parse(progressContext.timestamp))
      const event = buildTelemetryEvent({
        eventType: "skill_run_completed",
        userId: progressContext.userId,
        userName: telemetry.userName,
        userHandle: telemetry.userHandle,
        teamId: telemetry.teamId,
        teamName: telemetry.teamName,
        teamSlug: telemetry.teamSlug,
        teamIsPersonal: telemetry.teamIsPersonal,
        executionMode: telemetry.executionMode,
        runId: progressContext.runId,
        skillRunnerId: progressContext.devAgentId,
        skillName: progressContext.devAgentName,
        skillCanonicalPath: progressContext.skillRunnerCanonicalPath,
        costUsd: reportSummary?.costUsd ?? undefined,
        durationMs: Number.isFinite(durationMs) ? durationMs : undefined,
        successEvalResult: reportSummary?.successEvalResult ?? null
      })
      if (progressContext.controlPlaneMirrorTarget) {
        void relayTelemetryEventToControlPlane(event, progressContext.controlPlaneMirrorTarget).catch(() => {})
      } else {
        void emitTelemetryEvent(event).catch(() => {})
      }
    }
  }
  progressContext.runSnapshot = nextRun
}

async function loadWorkflowReportSummary(reportBlobUrl: string): Promise<WorkflowReportSummary | null> {
  "use step"
  try {
    const report = await readBlobJson<import("@/types").WorkflowReport>(reportBlobUrl)
    if (!report) {
      workflowLog(`[Workflow] Failed to load report summary from ${reportBlobUrl}`)
      return null
    }
    return {
      clsScore: report.clsScore,
      afterClsScore: report.afterClsScore,
      costUsd: report.costUsd,
      beforeWebVitals: report.beforeWebVitals,
      afterWebVitals: report.afterWebVitals,
      verificationStatus: report.verificationStatus,
      successEvalResult: report.successEvalResult,
      beforeScreenshots: report.beforeScreenshots,
      afterScreenshots: report.afterScreenshots,
      beforeScreenshotUrl: report.beforeScreenshotUrl,
      afterScreenshotUrl: report.afterScreenshotUrl
    }
  } catch (error) {
    workflowLog(
      `[Workflow] Failed to load report summary from ${reportBlobUrl}: ${error instanceof Error ? error.message : String(error)}`
    )
    return null
  }
}

